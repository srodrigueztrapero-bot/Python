### operadores aritméticos ###

print(2 + 3) # suma
print(5 - 2) # resta
print(4 * 3) # multiplicación
print(10 / 2) # división
print(10 % 3) # módulo
print (10 // 3) # división entera
print(2 ** 3) # potencia

print("Hola" + " " + "Python" + " " + "Que tal") # concatenación de strings
print("Hola" + " " + str(5)) # concatenación de string con número, se debe convertir el número a string
print("Hola " * 3 ** 2) # repetición de string

my_float = 2.5 * 2 # multiplicación de float
print("Hola " * int(my_float)) # repetición de string con float convertido a int

### operadores de comparación ###
print(5 > 3) # mayor que
print(5 < 3) # menor que
print(5 == 3) # igual que
print(5 != 3) # diferente de
print(5 >= 3) # mayor o igual que
print(5 <= 3) # menor o igual que

print("Hola" > "Python") # comparación de strings, se compara el valor ASCII de los caracteres
print("Hola" < "Python") # comparación de strings, se compara el valor ASCII de los caracteres
print("Hola" == "Hola") # comparación de strings, se compara el valor ASCII de
print("Hola" != "Python") # comparación de strings, se compara el valor ASCII de los caracteres
print("Hola" >= "Python") # comparación de strings, se compara el valor ASCII de los caracteres
print("Hola" <= "Python") # comparación de strings, se compara el valor ASCII de

### operadores lógicos ###
print(True and False) # operador AND
print(True or False) # operador OR
print(not True) # operador NOT
print(not False) # operador NOT
print((5 > 3) and (2 < 4)) # combinación de operadores de comparación y lógicos
print((5 > 3) or (2 > 4)) # combinación de operadores de comparación y lógicos
print(not (5 > 3)) # combinación de operadores de comparación y lógicos